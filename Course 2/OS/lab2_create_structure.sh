#!/bin/bash

#создание основной директории и подкаталогов
mkdir -p ISTOR/.res ISTOR/trans ISTOR/tiri ISTOR/diod ISTOR/vari ISTOR/sin

#назначение прав доступа согласно таблице
chmod u=rwx,g=r-x,o=r-x ISTOR/.res      # .res: владелец - чтение/запись/выполнение; группа и остальные - чтение/выполнение
chmod u=rwx,g=r-x,o=r-- ISTOR/trans     # trans: владелец - чтение/запись/выполнение; группа - чтение/выполнение; остальные - только чтение
chmod u=rwx,g=r--,o=--- ISTOR/tiri      # tiri: владелец - чтение/запись/выполнение; группа - только чтение; остальные - без прав
chmod u=rwx,g=r-x,o=r-x ISTOR/diod      # diod: владелец - чтение/запись/выполнение; группа и остальные - чтение/выполнение
chmod u=rwx,g=r--,o=--- ISTOR/vari      # vari: владелец - чтение/запись/выполнение; группа - только чтение; остальные - без прав
chmod u=rwx,g=r-x,o=r-- ISTOR/sin       # sin: владелец - чтение/запись/выполнение; группа - чтение/выполнение; остальные - только чтение

#проверка прав доступа
echo "Права доступа к структуре каталогов:"
ls -lR ISTOR
